-- Verificar la inserción
SELECT 
    category,
    COUNT(*) as total_productos,
    MIN(price) as precio_minimo,
    MAX(price) as precio_maximo
FROM products 
WHERE category IN ('PANES', 'MEDIALUNAS Y FACTURAS', 'BUDINES Y MUFFINS', 'DULCES', 'GALLETITAS Y VARIOS')
GROUP BY category
ORDER BY category;
